import React from 'react';
import CommentForm from "../components/CommentForm";

import user_logo from '../../public/user-icon.png';

type Comment = {
  id: string,
  userId: string,
  createdAt: string,
  username: string,
  body: string
}

type ActiveComment = {
  id: string,
  type: string,
  
}

type Props = {
  comment: Comment,
  replies: any,
  setActiveComment:any,
  activeComment: any,
  updateComment:any,
  deleteComment:any,
  addComment:any,
  parentId: any,
  currentUserId: string
};

const Comment = ({
  comment,
  replies,
  setActiveComment,
  activeComment,
  updateComment,
  deleteComment,
  addComment,
  parentId = null,
  currentUserId,
}: Props) => {
  const isEditing =
    activeComment &&
    activeComment.id === comment.id &&
    activeComment.type === "editing";
  const isReplying =
    activeComment &&
    activeComment.id === comment.id &&
    activeComment.type === "replying";
  const fiveMinutes = 300000;
  const currentDate:any = new Date();
  const commentCreatedAt:any = new Date(comment.createdAt);

  const timePassed = currentDate - commentCreatedAt > fiveMinutes;
  const canDelete =
    currentUserId === comment.userId && replies.length === 0 && !timePassed;
  const canReply = Boolean(currentUserId);

  console.log(comment)
  console.log("currentUserId "+currentUserId)
  console.log("comment.userId "+comment.userId)
  const canCommit = Boolean(currentUserId === comment.userId && parentId===null);

  const canEdit = currentUserId === comment.userId && !timePassed;
  const replyId = parentId ? parentId : comment.id;
  const createdAt = new Date(comment.createdAt).toLocaleDateString();
  return (
    <div key={comment.id} className="comment">
      <div className="comment-image-container">
        <img src={user_logo} /> 
      </div>
      <div className="comment-right-part">
        <div className="comment-content">
          <div className="comment-author">{comment.username}</div>
          <div>{createdAt}</div>
        </div>
        {!isEditing && <div className="comment-text">{comment.body}</div>}
        {isEditing && (
          <CommentForm
            submitLabel="Update"
            hasCancelButton
            initialText={comment.body}
            handleSubmit={(text:string) => updateComment(text, comment.id)}
            handleCancel={() => {
              setActiveComment(null);
            }}
          />
        )}
        <div className="comment-actions">
          {canReply && (
            <div
              className="comment-action"
              onClick={() =>
                setActiveComment({ id: comment.id, type: "replying" })
              }
            >
              Reply
            </div>
          )}
          {
            canCommit && (
                <div className="comment-action" onClick={() =>
                  setActiveComment({ id: comment.id, type: "editing" })
                }>COMMIT</div>
            )
          }
          {canEdit && (
            <div
              className="comment-action"
              onClick={() =>
                setActiveComment({ id: comment.id, type: "editing" })
              }
            >
              Edit
            </div>
          )}
          {canDelete && (
            <div
              className="comment-action"
              onClick={() => deleteComment(comment.id)}
            >
              Delete
            </div>
          )}
        </div>
        {isReplying && (
          <CommentForm
            submitLabel="Reply"
            handleSubmit={(text: any) => addComment(text, replyId)}
            handleCancel={(text: any) => addComment(text, replyId)} hasCancelButton={false} initialText={''}          />
        )}
        {replies.length > 0 && (
          <div className="replies">
            {replies.map((reply:any) => (
              <Comment
                comment={reply}
                key={reply.id}
                setActiveComment={setActiveComment}
                activeComment={activeComment}
                updateComment={updateComment}
                deleteComment={deleteComment}
                addComment={addComment}
                parentId={comment.id}
                replies={[]}
                currentUserId={currentUserId}
              />
            ))}
          </div>
        )}
      </div>
    </div>
  );
};

export default Comment;
